package Poglavlje08.Zadaci.Z0802;

/**
 * Klasa Kontakt predstavlja jedan kontakt u imeniku.
 */
public class Kontakt {

    private String ime;
    private String broj;

    /**
     * Metoda vraća telefonski broj pridružen kontaktu.
     *
     * @return telefonski broj kontakta.
     */
    public String getBroj() {
        return broj;
    }

    /**
     * Metoda vraća ime pridruženo kontaktu.
     *
     * @return ime kontakta.
     */
    public String getIme() {
        return ime;
    }

    /**
     * Metoda postavlja telefonski broj kontakta.
     *
     * @param broj telefonski broj kontakta.
     */
    public void setBroj(String broj) {
        this.broj = broj;
    }

    /**
     * Metoda postavlja ime kontakta.
     *
     * @param ime ime kontakta.
     */
    public void setIme(String ime) {
        this.ime = ime;
    }

}
