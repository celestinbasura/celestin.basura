/**
 * Vje�ba V0302
 * Napisati program koji ispisuje sve brojeve od 1 do 10 uzlazno i silazno
 * koriste�i petlju for.
 */
public class V0302 {

    public static void main(String[] args) {

        System.out.printf("\n --- Uzlazno od %d do %d --- \n", 1, 10);
        for(int i = 1; i <= 10; i++) {
            System.out.printf("%d, ", i);
        }

        System.out.printf("\n --- Silazno od %d do %d --- \n", 10, 1);
        for(int i = 10; i >= 1; i--) {
            System.out.printf("%d, ", i);
        }

    }
}
