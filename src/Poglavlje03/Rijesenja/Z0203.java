package Poglavlje02.Rijesenja;

/**
 * Zadatak Z0203 
 * Napisati program koji zbraja sve znamenke nekog cijelog 4-znamenkastog 
 * broja.
 *
 * Primjer:
 * Unesite broj: 3203
 * Suma znamenaka je: 8
 *
 * Naputak: Koristiti operacije dijeljenja i ostatka dijeljenja s bazom 10.
 */
public class Z0203 {

    public static void main(String[] args) {

        int broj = 1234;

        int z1 = broj % 10;
        int z2 = (broj / 10) % 10;
        int z3 = (broj / 100) % 10;
        int z4 = broj / 1000;

        System.out.println("Zbroj znamenaka broja " + broj + " je " + (z1 + z2 + z3 + z4));

    }
}