/**
 * P0321 Primjer do-while petlje, petlja ispisuje broj prolaska.
 */
public class P0321 {

    public static void main(String[] args) {

        int i = 0;

        do {
            System.out.println("Prolazak br: " + i);
            i++; // ovo je isto kao i = i + 1
        } while (i < 5);

    }
}