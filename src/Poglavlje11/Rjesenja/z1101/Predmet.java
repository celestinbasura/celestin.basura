package Poglavlje11.Zadaci.z1101;

/**
 * Klasa Predmet opisuje jedan predmet koji polaznik slu≈°a.
 */
public class Predmet implements Ispisljiv {

    private String sifra, naziv, predavac;

    public Predmet(String sifra, String naziv, String predavac) {
        this.sifra = sifra;
        this.naziv = naziv;
        this.predavac = predavac;
    }


    /**
     * Implenetacija metode ispisi ispisuje sve atribute polaznika.
     * @return stanje objekta
     */
    @Override
    public String ispisi() {
        return String.format("sifra:%s, naziv:%s, predavac:%s",
                sifra, naziv, predavac);
    }


}
