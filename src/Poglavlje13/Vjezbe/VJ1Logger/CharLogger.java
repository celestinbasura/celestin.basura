package Poglavlje13.Vjezbe.VJ1Logger;

/**
 *
 * @author Celestin
 */
public class CharLogger {
    
    public boolean isSamoglasnik(char a){
       
        Character bc = a;
        String str = bc.toString();
        a = str.toLowerCase().charAt(0);       
        if(a == 'a' || a == 'e' || a == 'i' || a == 'o' || a == 'u'){
            return true;
        }else{
            return false;
        }
        
    }
    public boolean isBroj(char a){
        
        
        if(a <= '\u0030' && a >= '\u0039'){
            return true;
        }else{
            return false;
        }
        
    }
    public boolean isSlovo(char a){
       if(Character.isLetter(Character.getNumericValue(a))){
           return true;
       }else{
           return false;
       }
       }
        
        
    }
