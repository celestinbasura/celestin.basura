public enum Mjesec {
    prvi(31),
    drugi(28),
    treci(31),
    cetvrti(30),
    peti(31), 
    sesti(30),
    sedmi(31),
    osmi(31),
    deveti(30),
    deseti(31),
    jedanaesti(30),
    dvaneasti(31);
   
    private int brojDana;

    public int getBrojDana(){
        return brojDana;
        
    }
    Mjesec (int i){
        
        this.brojDana = i;            
    }
    @Override
    public String toString(){
        
        return "Mjesec ima " + brojDana + " dana";
    }
    
}
